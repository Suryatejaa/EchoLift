const mongoose = require('mongoose');

const RecentSearchSchema = new mongoose.Schema({
    query: {
        type: String,
        required: true,
    },
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

module.exports = mongoose.model('RecentSearch', RecentSearchSchema);
